// Helper function to display lesson plan
function displayLessonPlan(topicKey) {
    const data = lessonData[topicKey];
    if (!data) {
        alert("Topic data not found! Please select another topic.");
        return;
    }
    
    document.getElementById('topic-title').textContent = data.title;
    
    // Display objectives
    const objectivesList = document.getElementById('objectives-list');
    objectivesList.innerHTML = data.objectives.map(obj => `<li>${obj}</li>`).join('');
    
    // Display vocabulary
    const vocabList = document.getElementById('vocabulary-list');
    vocabList.innerHTML = data.vocabulary.map(vocab => `
        <div class="vocab-item">
            <span class="vocab-word">${vocab.word}</span>
            <span class="vocab-meaning">${vocab.meaning}</span>
        </div>
    `).join('');
    
    // Display materials
    const materialsList = document.getElementById('materials-list');
    materialsList.innerHTML = data.materials.map(m => `<li>${m}</li>`).join('');
    
    // Display time and group info
    document.getElementById('time-info').textContent = `⏱️ Total Time: ${data.time}`;
    document.getElementById('group-info').textContent = `👥 Group Size: ${data.group}`;
    
    // Display activities
    displayActivities('corner-activities', data.corner);
    displayActivities('craft-activities', data.craft);
    displayActivities('play-activities', data.play);
    displayActivities('story-activities', data.stories);
    displayActivities('teaching-activities', data.teaching);
    displayActivities('display-activities', data.display);
    
    // Display Q&A
    const qaSection = document.getElementById('qa-section');
    qaSection.innerHTML = data.qa.map(qa => `
        <div class="qa-item">
            <div class="qa-question">${qa.question}</div>
            <div class="qa-answer">${qa.answer}</div>
        </div>
    `).join('');
    
    // Display discussion questions
    const discussionList = document.getElementById('discussion-list');
    discussionList.innerHTML = data.discussion.map(q => `<li>${q}</li>`).join('');
    
    // Display parent tips
    const parentTips = document.getElementById('parent-tips');
    parentTips.innerHTML = data.parentTips.map(tip => `<li>${tip}</li>`).join('');
    
    // Display assessment
    const assessmentList = document.getElementById('assessment-list');
    assessmentList.innerHTML = data.assessment.map(a => `<li>${a}</li>`).join('');
    
    // Set current date
    document.getElementById('current-date').textContent = new Date().toLocaleDateString('en-PK');
}

function displayActivities(containerId, activities) {
    const container = document.getElementById(containerId);
    container.innerHTML = activities.map(activity => `
        <div class="activity-item">
            <div class="activity-title">${activity.idea}</div>
            <button class="toggle-process">📋 Show Process ▼</button>
            <div class="process" style="display:none">
                <span class="process-title">How to do it:</span>
                ${activity.process.replace(/\n/g, '<br>')}
            </div>
        </div>
    `).join('');
    
    // Add toggle functionality
    container.querySelectorAll('.toggle-process').forEach(btn => {
        btn.addEventListener('click', function() {
            const processDiv = this.nextElementSibling;
            if (processDiv.style.display === 'none') {
                processDiv.style.display = 'block';
                this.innerHTML = '📋 Hide Process ▲';
            } else {
                processDiv.style.display = 'none';
                this.innerHTML = '📋 Show Process ▼';
            }
        });
    });
}

